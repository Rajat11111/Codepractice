package octra;

public class Calculator {

	public static void main(String[] args) {
		int num1 = 10;
		int num2 = 20;
		
		char operator = '*';
		
		if (operator == '+'){
			System.out.println(num1 + num2);
			
		}else if (operator == '-'){
			System.out.println(num1 - num2);
			
		}else if (operator == '*'){
			System.out.println(num1*num2);
			
		}else if (operator == '/'){
			System.out.println(num1/num2);
		}
		
		
				
		

	}

}
